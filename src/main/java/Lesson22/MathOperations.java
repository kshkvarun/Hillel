package Lesson22;

public class MathOperations {
    public int sum(int i, int j) {
        return i + j;
    }

    public int subtraction(int i, int j) {
        return i - j;
    }

    public int multiplying(int i, int j) {
        return i * j;
    }

    public double dividing(double i, double j) {
        return i / j;
    }

    public int dividing(int i, int j) {
        return i / j;
    }

    public String someString(String a, String b, String c) {
        return a + b + c;
    }

}
